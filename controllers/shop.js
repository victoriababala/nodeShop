exports.getCheckout = (req, res, next) => {
  let products;
  let total = 0;
  req.user
    .populate("cart.items.productId")
    .then((user) => {
      products = user.cart.items;
      total = 0;
      products.forEach((p) => {
        total += p.quantity * p.productId.price;
      });

      return stripe.checkout.sessions.create({
        payment_method_types: ["card"],
        line_items: products.map((p) => {
          /** CHANGED STRUCTURE FOR STRIPE */
          return {
            price_data: {
              product_data: {
                name: p.productId.title,
                description: p.productId.description,
              },
              unit_amount: p.productId.price * 100,
              currency: "usd",
            },
            quantity: p.quantity,
          };
        }),
        /** INCLUDE 'mode' KEY */
        mode: "payment",
        success_url:
          req.protocol + "://" + req.get("host") + "/checkout/success", // http://localhost:2000/checkout/success
        cancel_url: req.protocol + "://" + req.get("host") + "/checkout/cancel",
      });
    })
    .then((session) => {
      res.render("shop/checkout", {
        path: "/checkout",
        pageTitle: "Checkout",
        products: products,
        totalSum: total,
        sessionId: session.id,
      });
    })
    .catch((err) => {
      const error = new Error(err);
      error.httpStatusCode = 500;
      return next(error);
    });
};

exports.getInvoice = (req, res, next) => {
  const orderId = req.params.orderId;
  Order.findById(orderId)
    .then((order) => {
      if (!order) {
        return next(new Error("No order found."));
      }
      if (order.user.userId.toString() !== req.user._id.toString()) {
        return next(new Error("Unauthorized."));
      }
      const invoiceName = "invoice-" + orderId + ".pdf";
      const invoicePath = path.join("data", "invoices", invoiceName);
      const doc = new document();
      res.setHeader("Content-Type", "application/pdf");
      res.setHeader(
        "Content-Disposition",
        'inline; filename="' + invoiceName + '"'
      );

      // Pipe the document to both a file and the response
      doc.pipe(fs.createWriteStream(invoicePath));
      doc.pipe(res);

      // Header
      doc
        .fontSize(30)
        .font("Helvetica-Bold")
        .text("INVOICE", { align: "center", underline: true })
        .moveDown();

      // Divider line
      doc
        .moveDown()
        .strokeColor("#aaaaaa")
        .lineWidth(1)
        .moveTo(doc.page.margins.left, doc.y)
        .lineTo(doc.page.width - doc.page.margins.right, doc.y)
        .stroke()
        .moveDown();

      // Product Table Header
      doc
        .fontSize(14)
        .font("Helvetica-Bold")
        .text("Product", { width: 200, continued: true })
        .text("Quantity", { width: 100, align: "center", continued: true })
        .text("Price", { width: 100, align: "right" })
        .moveDown();

      // Product List
      let totalPrice = 0;
      order.products.forEach((prod) => {
        totalPrice += prod.quantity * prod.product.price;
        doc
          .fontSize(12)
          .font("Helvetica")
          .text(prod.product.title, { width: 200, continued: true })
          .text(prod.quantity, { width: 100, align: "center", continued: true })
          .text("$" + prod.product.price.toFixed(2), {
            width: 100,
            align: "right",
          });
      });

      // Divider line
      doc
        .moveDown()
        .strokeColor("#aaaaaa")
        .lineWidth(1)
        .moveTo(doc.page.margins.left, doc.y)
        .lineTo(doc.page.width - doc.page.margins.right, doc.y)
        .stroke()
        .moveDown();

      // Total Price
      doc
        .fontSize(16)
        .font("Helvetica-Bold")
        .text("Total Price: $" + totalPrice.toFixed(2), { align: "right" })
        .moveDown();

      // Footer
      doc
        .fontSize(10)
        .font("Helvetica-Oblique")
        .text("Thank you for your business!", { align: "center" })
        .moveDown();

      // End the document
      doc.end();
    })
    .catch((err) => next(err));
};

